#Tipos de dados em Ruby
#O Tipo de uma variável é dinâmico, ou seja, definido quando a variável é criada e recebe o valor. 

produto = 'Macbook Pro'
#O tipo string foi atribuido, ao definir um texto como o valor da variável produto. 
preco = 5999.99
quantidade = 10
disponivel = true

#Por ser dinamica, ao atribuir um valor de tipo diferente, o tipo da classe pode ser alterado. 
#Para descobrir o tipo da variável.
puts produto.class
puts preco.class
puts quantidade.class
puts disponivel.class

puts produto
